package model;

public class Course {
	String course_instance;
	String coursename;
	public String getCourse_instance() {
		return course_instance;
	}
	public void setCourse_instance(String course_instance) {
		this.course_instance = course_instance;
	}
	public String getCoursename() {
		return coursename;
	}
	public void setCoursename(String coursename) {
		this.coursename = coursename;
	}
}
